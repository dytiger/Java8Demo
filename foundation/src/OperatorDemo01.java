public class OperatorDemo01 {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a/2);// 2
        double b = 5;
        System.out.println(b/2);// 2.5

        System.out.println(a%2);// 1
        System.out.println(5.5%2);// 1.5

        double c = 5.5;
        System.out.println(++c);// 6.5 先加后用
    }
}
